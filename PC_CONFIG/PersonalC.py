class PersonalC:

    def __init__(self, mother, cpu, gpu, ram, rom, psu, cooler, case):

        #Материнская плата
        self.mother = mother
        # Процессор
        self.cpu = cpu
        # Графический процессор
        self.gpu = gpu
        #Оперативная память
        self.ram = ram
        #Постоянная память 
        self.rom = rom
        #Блок питания
        self.psu = psu
        #Кулер процессорный
        self.cooler = cooler 
        #Корпус
        self.case = case
